/** Fast lookup map by partNumber or SKU for work-order autocomplete */
export const PARTS_BY_NUMBER = new Map<string, CatalogPart>(
  PARTS_CATALOG.flatMap(p => {
    const entries: [string, CatalogPart][] = [[p.sku.toLowerCase(), p]];
    if (p.partNumber) entries.push([p.partNumber.toLowerCase(), p]);
    return entries;
  })
);

/** Search parts by name, SKU, or part number (case-insensitive) */
export function searchParts(query: string): CatalogPart[] {
  if (!query.trim()) return [];
  const q = query.toLowerCase();
  return PARTS_CATALOG.filter(
    p =>
      p.name.toLowerCase().includes(q) ||
      p.sku.toLowerCase().includes(q) ||
      (p.partNumber && p.partNumber.toLowerCase().includes(q)) ||
      p.description.toLowerCase().includes(q)
  ).slice(0, 10);
}
